function get_com_no_mid_pose(N; sl=0.25) 
    cid = [i for i=0:N]
    poses = [sl*i for i in cid]
    return poses 
end

function get_com_poses(N; sl=0.25)
    poses = [0., 0.]
    cid = [i for i=0:N]
    for i in cid 
        r = sl*i 
        push!(poses, r)
        push!(poses, r)
    end
    return poses
end
